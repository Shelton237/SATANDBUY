const verifyEmailAddress = async (req, res) => {
  const isAdded = await Customer.findOne({ email: req.body.email });
  if (isAdded) {
    return res.status(403).send({
      message: "This Email already Added!",
    });
  } else {
    const token = tokenForVerify(req.body);
    const option = {
      name: req.body.name,
      email: req.body.email,
      token: token,
    };
    const body = {
      from: process.env.EMAIL_USER,
      // from: "info@demomailtrap.com",
      to: `${req.body.email}`,
      subject: "Email Activation",
      subject: "Verify Your Email",
      html: customerRegisterBody(option),
    };

    const message = "Please check your email to verify your account!";
    sendEmail(body, res, message);
  }
};

const registerCustomer = async (req, res) => {
  const token = req.params.token;
  const { name, email, password } = jwt.decode(token);
  const isAdded = await Customer.findOne({ email: email });

  if (isAdded) {
    const token = signInToken(isAdded);
    return res.send({
      token,
      _id: isAdded._id,
      name: isAdded.name,
      email: isAdded.email,
      role: isAdded.role || CLIENT_ROLE,
      message: "Email Already Verified!",
    });
  }

  if (token) {
    jwt.verify(token, process.env.JWT_SECRET_FOR_VERIFY, (err, decoded) => {
      if (err) {
        return res.status(401).send({
          message: "Token Expired, Please try again!",
        });
      } else {
        const newUser = new Customer({
          name,
          email,
          password: bcrypt.hashSync(password),
          role: CLIENT_ROLE,
        });
        newUser.save();
        const token = signInToken(newUser);
        res.send({
          token,
          _id: newUser._id,
          name: newUser.name,
          email: newUser.email,
          role: CLIENT_ROLE,
          message: "Email Verified, Please Login Now!",
        });
      }
    });
  }
};

const registerCustomerDirect = async (req, res) => {
  try {
    const { name, email, password } = req.body;
    if (!name || !email || !password) {
      return res.status(400).send({ message: "Name, email and password are required." });
    }
    const existing = await Customer.findOne({ email });
    if (existing) {
      return res.status(409).send({ message: "This email is already registered!" });
    }

    const newUser = await Customer.create({
      name,
      email,
      password: bcrypt.hashSync(password),
      role: CLIENT_ROLE,
    });

    const token = signInToken(newUser);
    res.send({
      token,
      _id: newUser._id,
      name: newUser.name,
      email: newUser.email,
      role: CLIENT_ROLE,
      message: "Account created successfully!",
    });
  } catch (err) {
    res.status(500).send({ message: err.message });
  }
};

